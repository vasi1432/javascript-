// var data and its types
// var,let,const

var age = 30;
var age = 22;
console.log(age);
// as we can see we reinitialise and redeclare the variable in var
// we can access var before its initialization but it would be undefined
let number = 30;
let number2 = 30;
// we cannot redeclare the variable in let
// but re-initialize the variable
// we cannot access let before its initialisation
console.log(number);
// we canot either reinitiale or redeclare the value in const
// the variable and value should be constant
// we cannot access const before its initialisation
const num = 1;
const num2 = 2;
console.log(num);
